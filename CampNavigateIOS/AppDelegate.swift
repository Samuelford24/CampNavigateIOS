        appId: "074bc731-1163-47e9-a665-025b3ebf7d08",
        handleNotificationAction: nil,
        settings: onesignalInitSettings)

        OneSignal.inFocusDisplayType = OSNotificationDisplayType.notification;
        FirebaseApp.configure()
        return true
